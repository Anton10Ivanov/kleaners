export type DesignTokens = typeof designTokens;

// Utility functions for accessing tokens
export const getSpacing = (key: keyof typeof designTokens.spacing) => designTokens.spacing[key];
export const getColor = (category: keyof typeof designTokens.colors, shade?: string | number) => {
  const colorCategory = designTokens.colors[category];
  if (typeof colorCategory === 'string') return colorCategory;
  if (shade && typeof colorCategory === 'object') {
    return (colorCategory as any)[shade];
  }
  return colorCategory;
};
export const getFontSize = (key: keyof typeof designTokens.typography.fontSizes) => 
  designTokens.typography.fontSizes[key];
